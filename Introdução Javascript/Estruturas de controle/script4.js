const dia = Number(prompt("Digite um número de um a 7: ").trim());

switch (dia) {
    case 1:
        alert("Domigo");
        break;
    case 2:
        alert("Segunda");
        break;
    case 3:
        alert("Terça");
        break;
    case 4:
        alert("Quarta");
        break;
    case 5:
        alert("Quinta");
        break;
    case 6:
        alert("Sexta");
        break;
    case 7:
        alert("Sábado");
        break;
    default:
        alert("Opção Inválida!");
}