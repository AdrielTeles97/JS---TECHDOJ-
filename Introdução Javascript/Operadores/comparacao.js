const numero1 = 4
const numero2 = "4"
console.log(numero1 == numero2) //IGUALDADE
console.log(numero1 !== numero2); //DIFERENÇA | CONTRARIO

console.log(numero1 > numero2) //MAIOR QUE
console.log(numero1 < numero2) //MENOR QUE
console.log(numero1 >= numero2) //MAIOR OU IGUAL , SÓ FUNCIONA EM COMPARAÇÃO DE NUMBERS
console.log(numero1 <= numero2) //MENOR OU IGUAL, SÓ FUNCIONA EM COMPARAÇÃO DE NUMBERS

console.log(numero1 | numero2) //OU




