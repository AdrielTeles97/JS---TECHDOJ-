const nota1 = 5
const nota2 = 6
const nota3 = 10
const nota4 = 7

const media = (nota1 + nota2 + nota3 + nota4) / 4
if (media >= 7) {
    console.log(`Parabéns sua média foi ${media.toFixed(2)}. APROVADO!`)
} else {
    console.log(`Que pena. Sua média foi ${media.toFixed(2)}. REPROVADO!`)
}

